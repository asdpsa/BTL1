    private void loaddata() {
        allLop = new LopHocDAO().getAll();
        for (LopHoc lh : allLop) {
            dtm.addRow(toObjectsData(lh));
        }
        jtbLopHoc.setModel(dtm);
    }

    private static Object[] toObjectsData(LopHoc lh) {
        Object[] objectsdata = {lh.getMalop(), lh.getTenlop(), lh.getMakhoa(), lh.getPhongHoc()};
        return objectsdata;
    }

