    private void bttDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttDeleteActionPerformed
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                String malop = maLop.getText();
                new LopHocDAO().deleteLopHoc(malop);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Không thể xóa vì trong lớp còn sinh viên!!");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy khóa học!!");
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            resetForm();
            loaddata();
            bttAddNew.setEnabled(true);
            bttDelete.setEnabled(false);
            bttEdit.setEnabled(false);
            bttRefresh.setEnabled(true);
        }
    }//GEN-LAST:event_bttDeleteActionPerformed

    private void bttAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAddNewActionPerformed
        String malop = maLop.getText();
        String tenlop = tenLop.getText();
        String makhoa = allKhoa.get(jcbMaKhoa.getSelectedIndex()).getMakhoa();
        String phonghoc = phongHoc.getText();
        String magv = allGV.get(jcbMaGV.getSelectedIndex()).getMaGV();
        LopHoc lh = new LopHoc(malop, tenlop, magv, makhoa, phonghoc);
        ArrayList<LopHoc> listCheck = new LopHocDAO().checkID(malop);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            maLop.setText("");
            maLop.requestFocus();
            return;
        } else if (malop == null || malop.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã lớp trống!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            maLop.requestFocus();
            return;
        } else if (tenlop == null || tenlop.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên lớp trống!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            tenLop.requestFocus();
            return;
        } else if (phonghoc == null || phonghoc.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa có phòng học!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            phongHoc.requestFocus();
            return;
        } else {
            LopHoc insert = new LopHocDAO().addNew(lh);
            if (insert != null) {
                showAll();
            }
        }
    }//GEN-LAST:event_bttAddNewActionPerformed

    private void bttEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEditActionPerformed
        String malop = maLop.getText();
        String tenlop = tenLop.getText();
        String makhoa = allKhoa.get(jcbMaKhoa.getSelectedIndex()).getMakhoa();
        String phonghoc = phongHoc.getText();
        String magv = allGV.get(jcbMaGV.getSelectedIndex()).getMaGV();
        LopHoc lh = new LopHoc(malop, tenlop, magv, makhoa, phonghoc);
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn thay đổi thông tin về lớp này ?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            if (tenlop == null || tenlop.equals("")) {
                JOptionPane.showMessageDialog(this, "Tên lớp trống!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
                tenLop.requestFocus();
                return;
            } else if (phonghoc == null || phonghoc.equals("")) {
                JOptionPane.showMessageDialog(this, "Chưa có phòng học!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
                phongHoc.requestFocus();
                return;
            } else {
                LopHoc update = new LopHocDAO().updateByID(lh);
                if (update != null) {
                    showAll();
                }
            }
        }
        bttAddNew.setEnabled(true);
        bttEdit.setEnabled(false);
        bttDelete.setEnabled(false);
        bttRefresh.setEnabled(true);
    }//GEN-LAST:event_bttEditActionPerformed

    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        int selectedIndex = jcbMaKhoa.getSelectedIndex();
//        Khoa getkhoa = allKhoa.get(selectedIndex);
        ArrayList<LopHoc> lhs = new LopHocDAO().getAll();
        for (LopHoc lopHoc : lhs) {
            Vector v = new Vector();
            v.add(lopHoc.getMalop());
            v.add(lopHoc.getTenlop());
            v.add(lopHoc.getMaGV());
            v.add(lopHoc.getMakhoa());
            v.add(lopHoc.getPhongHoc());
            dtm.addRow(v);
        }
        resetForm();
    }
