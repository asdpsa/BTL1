    private void bttDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttDeleteActionPerformed
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                String makhoa = maKhoa.getText();
                new KhoaDAO().deleteKhoa(makhoa);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Không thể xóa!!");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy khóa học!!");
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            resetForm();
            loaddata();
            bttAddNew.setEnabled(true);
            bttEdit.setEnabled(false);
            bttDelete.setEnabled(false);
            maKhoa.setEnabled(true);
        }
    }//GEN-LAST:event_bttDeleteActionPerformed

    private void bttAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAddNewActionPerformed
        String makhoa = maKhoa.getText();
        String tenkhoa = tenKhoa.getText();
        String sdt = thoiGian.getText();
        Khoa k = new Khoa(makhoa, tenkhoa, sdt);
        ArrayList<Khoa> listCheck = new KhoaDAO().checkID(makhoa);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            maKhoa.setText("");
            maKhoa.requestFocus();
            return;
        } else if (makhoa == null || makhoa.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã khóa học trống!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            maKhoa.requestFocus();
            return;
        } else if (tenkhoa == null || tenkhoa.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên khóa học trống", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            tenKhoa.requestFocus();
            return;
        } else if (sdt == null || sdt.equals("")) {
            JOptionPane.showMessageDialog(this, "Thời gian học trống", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            thoiGian.requestFocus();
            return;
        } else {
            Khoa insert = new KhoaDAO().addNew(k);
            if (insert != null) {
                showAll();
            }
        }
        resetForm();
    }//GEN-LAST:event_bttAddNewActionPerformed

    private void bttEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEditActionPerformed
        String makhoa = maKhoa.getText();
        String tenkhoa = tenKhoa.getText();
        String sdt = thoiGian.getText();
        Khoa k = new Khoa(makhoa, tenkhoa, sdt);
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn thay đổi thông tin về khóa học này ?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            if (tenkhoa == null || tenkhoa.equals("")) {
                JOptionPane.showMessageDialog(this, "Tên khóa học trống", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
                tenKhoa.requestFocus();
                return;
            } else if (sdt == null || sdt.equals("")) {
                JOptionPane.showMessageDialog(this, "Thời gian học trống", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
                thoiGian.requestFocus();
                return;
            } else {
                Khoa update = new KhoaDAO().updateByID(k);
                if (update != null) {
                    showAll();
                }
            }
        }
        bttAddNew.setEnabled(true);
        bttEdit.setEnabled(false);
        bttDelete.setEnabled(false);
        maKhoa.setEnabled(true);
    }//GEN-LAST:event_bttEditActionPerformed


