    private void loaddata() {
        for (Khoa khoa : list) {
            dtm.addRow(toObjectsData(khoa));
        }
        jtbKhoa.setModel(dtm);
    }

    private static Object[] toObjectsData(Khoa k) {
        Object[] objectsData = {k.getMakhoa(), k.getTenkhoa(), k.getThoiGian()};
        return objectsData;
    }

    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<Khoa> khoas = new KhoaDAO().getAll();
        for (Khoa khoa : khoas) {
            Vector v = new Vector();
            v.add(khoa.getMakhoa());
            v.add(khoa.getTenkhoa());
            v.add(khoa.getThoiGian());
            dtm.addRow(v);
        }
    }

