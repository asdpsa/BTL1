    private void jcbMonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbMonActionPerformed
        while (dtmTK.getRowCount() > 0) {
            dtmTK.removeRow(0);
        }
        String maLop = jcbLop.getSelectedItem().toString();
        String maMon = jcbMon.getSelectedItem().toString();
        ArrayList<SinhVien> sinhVien = new SinhVienDAO().findByIDLop(maLop);
        for (SinhVien sv : sinhVien) {
            Vector v = new Vector();
            v.add(sv.getMaSV());
            v.add(sv.getTenSV());
            ArrayList<Diem> bangDiem = new DiemDAO().loaddiem(sv.getMaSV(), maMon);
            for (Diem d : bangDiem) {
                v.add(d.getDiem());
                if (d.isTrangThai() == true) {
                    v.add("Đạt");
                } else {
                    v.add("Không Đạt");
                }
            }
            dtmTK.addRow(v);
        }
    }//GEN-LAST:event_jcbMonActionPerformed

