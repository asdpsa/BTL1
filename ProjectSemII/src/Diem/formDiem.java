    private static Object[] toObjectsDataSV(SinhVien sv) {
        Object[] objectsData = {sv.getMaSV(), sv.getTenSV()};
        return objectsData;
    }

    public void showAll() {
        while (dtmDiem.getRowCount() > 0) {
            dtmDiem.removeRow(0);
        }
        String masv = jtbDSSV.getValueAt(jtbDSSV.getSelectedRow(), 0).toString();
        ArrayList<Diem> bangDiem = new DiemDAO().findByIDSinhVien(masv);
        for (Diem bd : bangDiem) {
            Vector v = new Vector();
            ArrayList<MonHoc> monHoc = new MonHocDAO().findIDMaMH(bd.getMaMH());
            for (MonHoc monHoc1 : monHoc) {
                v.add(monHoc1.getTenMH());
            }
            v.add(bd.getMaMH());
            v.add(bd.getLanThi());
            v.add(bd.getDiem());
            if (bd.isTrangThai() == true) {
                v.add("Đạt");
            } else {
                v.add("Không Đạt");
            }
            dtmDiem.addRow(v);
        }
        resetForm();
    }

