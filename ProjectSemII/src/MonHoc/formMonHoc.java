    private void bttAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAddNewActionPerformed
        String mamh = maMH.getText();
        String tenmh = tenMH.getText();
        String sogio = soGio.getText();
        String htt = (String) hTT.getSelectedItem();
        int sgio = 0;
        try {
            sgio = Integer.parseInt(sogio);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Số giờ không đúng định dạng số!!", "Vui lòng kiểm tra lại!!", JOptionPane.ERROR_MESSAGE);
            soGio.requestFocus();
            return;
        }
        MonHoc monHoc = new MonHoc(mamh, tenmh, sgio, htt);
        ArrayList<MonHoc> listCheck = new MonHocDAO().CheckID(mamh);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            maMH.setText("");
            maMH.requestFocus();
            return;
        } else if (mamh == null || mamh.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã môn học trống", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            maMH.requestFocus();
            return;
        } else if (tenmh == null || tenmh.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên môn học trống", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            tenMH.requestFocus();
            return;
        } else {
            MonHoc insert = new MonHocDAO().addNew(monHoc);
            if (insert != null) {
                showAll();
            }
        }
    }//GEN-LAST:event_bttAddNewActionPerformed

    private void bttEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEditActionPerformed
        String mamh = maMH.getText();
        String tenmh = tenMH.getText();
        String sogio = soGio.getText();
        String htt = (String) hTT.getSelectedItem();
        int sgio = 0;
        try {
            sgio = Integer.parseInt(sogio);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Số giờ không đúng định dạng số!!", "Vui lòng kiểm tra lại!!", JOptionPane.ERROR_MESSAGE);
            soGio.requestFocus();
            return;
        }
        MonHoc monHoc = new MonHoc(mamh, tenmh, sgio, htt);
        MonHoc update = new MonHocDAO().updateByID(monHoc);
        if (tenmh == null || tenmh.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên môn học trống!!", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            tenMH.requestFocus();
            return;
        } else if (update != null) {
            showAll();
        }
        bttAddNew.setEnabled(true);
        bttEdit.setEnabled(false);
        bttDelete.setEnabled(false);
        bttRefresh.setEnabled(true);
        maMH.setEnabled(true);
    }//GEN-LAST:event_bttEditActionPerformed

    private void bttDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttDeleteActionPerformed
        String mamh = maMH.getText();
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                new MonHocDAO().delMonHoc(mamh);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, có tồn tại điểm của môn này, hãy xóa dữ liệu điểm trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, có tồn tại điểm của môn này, hãy xóa dữ liệu điểm trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            resetForm();
            loaddata();
            bttAddNew.setEnabled(true);
            bttEdit.setEnabled(false);
            bttDelete.setEnabled(false);
            bttRefresh.setEnabled(true);
        }
    }//GEN-LAST:event_bttDeleteActionPerformed

    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<MonHoc> hocs = new MonHocDAO().getAll();
        for (MonHoc mh : hocs) {
            Vector v = new Vector();
            v.add(mh.getMaMH());
            v.add(mh.getTenMH());
            v.add(mh.getSoGio());
            v.add(mh.getHinhThucThi());
            dtm.addRow(v);
        }
        resetForm();
    }

