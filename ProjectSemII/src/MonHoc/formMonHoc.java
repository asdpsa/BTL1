    private void loaddata() {
        list = new MonHocDAO().getAll();
        for (MonHoc mh : list) {
            dtm.addRow(toObjectsData(mh));
        }
        jtbMH.setModel(dtm);
    }

    public static Object[] toObjectsData(MonHoc mh) {
        Object[] objectsData = {mh.getMaMH(), mh.getTenMH(), mh.getSoGio(), mh.getHinhThucThi()};
        return objectsData;
    }

